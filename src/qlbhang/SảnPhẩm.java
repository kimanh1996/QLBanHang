    public void tablesanpham(String sql) {
        Vector rowheader = new Vector();
        rowheader.add("Mã Sản Phẩm");
        rowheader.add("Tên Sản Phẩm");
        rowheader.add("Loại Sản Phẩm");
        rowheader.add("Hãng Sản Xuất");
        rowheader.add("Gía Nhập ");
        rowheader.add("Gía Bán");
        rowheader.add("Tồn Kho");
        rowheader.add("Trạng Thái");
        rowheader.add("Ghi Chú");
        Vector tmp = new Vector();
        ResultSet rs = db.getData(sql);
        try {
            while (rs.next()) {
                Vector rowdata = new Vector();
                rowdata.add(rs.getInt("MaSanPham"));
                rowdata.add(rs.getString("TenSanPham"));
                rowdata.add(rs.getString("TenLoaiSanPham"));
                rowdata.add(rs.getString("TenHangSanXuat"));
                // NumberFormat fm = NumberFormat.getCurrencyInstance(new Locale("vi", "VN"));
                rowdata.add(rs.getInt("GiaNhap"));
                rowdata.add(rs.getInt("GiaBan"));
                rowdata.add(rs.getInt("TonKho"));
                rowdata.add(rs.getInt("TrangThai"));
                rowdata.add(rs.getString("GhiChu"));

                tmp.add(rowdata);

            }
        } catch (SQLException ex) {
            Logger.getLogger(HóaĐơn.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbl_sanpham.setModel(new DefaultTableModel(tmp, rowheader));

    }

