    private void tbl_sanphamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_sanphamMouseClicked

        txtMasp1.setEditable(false);
        int index = tbl_sanpham.getSelectedRow();

        txtMasp1.setText(tbl_sanpham.getValueAt(index, 0).toString());
        txtTenSanPham1.setText(tbl_sanpham.getValueAt(index, 1).toString());
        db.SetSelectedCombobox(tbl_sanpham.getValueAt(index, 2).toString(), cbb_loaisp);
        db.SetSelectedCombobox(tbl_sanpham.getValueAt(index, 3).toString(), cbb_hangsanxuat);
        txtGiaNhap.setText(tbl_sanpham.getValueAt(index, 4).toString());
        txtGiaBan.setText(tbl_sanpham.getValueAt(index, 5).toString());
        txttonkho.setText(tbl_sanpham.getValueAt(index, 6).toString());
        String trangthai = tbl_sanpham.getValueAt(index, 7).toString();
        if (trangthai.equalsIgnoreCase("Con Hang")) {
            radio_con.setEnabled(true);
            radio_het.setEnabled(false);
        } else {
            radio_con.setEnabled(false);
            radio_het.setEnabled(true);
        }
        //  txtghichu.setText(tbl_sanpham.getValueAt(index, 8).toString());


    }//GEN-LAST:event_tbl_sanphamMouseClicked

